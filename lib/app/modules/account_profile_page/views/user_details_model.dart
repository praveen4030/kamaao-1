class UserDetailsModel {
  final String name;
  final String email;
  final String phone;
  final String address;
  final String image;
  final DateTime dob;
  final String gender;
  final String education;
  final String experience;
  UserDetailsModel({
    required this.name,
    required this.email,
    required this.phone,
    required this.address,
    required this.image,
    required this.dob,
    required this.education,
    required this.experience,
    required this.gender,
  });
}
